<p class="morep" style="text-align:left;font-family:roboto-medium;font-size:120%;">Who is a campus ambassador ?</p>

<p class="morep" style="text-align:left;font-size:120%;">The Exodia &#39;16 Campus Ambassador Programme is an exclusive opportunity for current college students to gain valuable experience of being an Exodia Ambassador in their college. It is a platform where you can make a difference by contributing, but at the same time enhance your public-relations skills &amp; expertise. Exodia Campus Ambassadors will learn valuable skills by implementing the action plans for promoting Exodia &#39;16 and its associated events on their campus.</p>

<p class="morep" style="text-align:left;font-size:120%;">If you are a passionate individual with ambitious dreams and would like to work with the talented and motivated team-members from one of the best technical institutes in India, then the &#39;Exodia &#39;16 Campus Ambassador Programme&#39; could be the right break for you. </p>

<p class="morep" style="text-align:left;font-family:roboto-medium;font-size:120%;">What you do:</p>

<p class="morep" style="text-align:left;font-size:120%;"></p>

<ul class="morep" style="text-align:left;font-size:120%;">
	<li>Be the official ambassador and representative of Exodia &#39;16 in your college.</li>
	<li>Coordinate the execution of activities of Exodia &#39;16 in your college.</li>
	<li>Publicize events, campaigns and workshops of Exodia &#39;16 on all social networking platforms of your college and its student-bodies.</li>
	<li>Maintain relevant databases and share them with Team Exodia &#39;16.</li>
</ul>

<p></p>

<p class="morep" style="text-align:left;font-family:roboto-medium;font-size:120%;">What you need: </p>

<p class="morep" style="text-align:left;font-size:120%;"></p>

<ul class="morep" style="text-align:left;font-size:120%;">
	<li>Moderate written and verbal communication skills.</li>
	<li>Excellent coordination and interaction skills.</li>
	<li>Motivation and zeal to come up with fresh ideas to improve our relationship.</li>
</ul>

<p></p>

<p class="morep" style="text-align:left;font-family:roboto-medium;font-size:120%;">What you get: </p>

<p class="morep" style="text-align:left;font-size:120%;"></p>

<ul class="morep" style="text-align:left;font-size:120%;">
	<li>Certificate of appreciation from Exodia &#39;16, IIT Mandi.</li>
	<li>Chance to visit IIT Mandi Campus for Exodia &#39;16.</li>
	<li>Free accomodation and food (provided there are 40+ participants from your college in Exodia &#39;16).</li>
	<li>Exclusive Exodia &#39;16 Merchandise.</li>
	<li>Real-world exposure and experience.</li>
</ul>

