                <h3>How to Reach</h3>
<p style=text-align:justify>
The usual travel route to Mandi is via Delhi or Chandigarh.
<ul><strong>From Delhi:</strong></ul>
<ul style=text-align:justify><strong> By bus/taxi:</strong> From ISBT (Interstate bus terminal) platforms 6-8, Kashmere gate, Delhi, one can catch buses towards Manali or Mandi. You can opt for different types of buses ranging from ordinary, to deluxe to AC. For more details, please visit hrtc.gov.in (Himachal Road Transport Corporation website). Other state buses and private buses are also available. The road distance between Delhi and Mandi is ~475 km. and it usually takes approximately 12 hrs (depending on traffic and other conditions) by bus. Traveling by taxi or personal vehicle, reduces the journey time by nearly an hour.<br>
<strong> By train: </strong> The alternative to reach Mandi from Delhi is to travel by train up to Kiratpur Sahib by Delhi-Una Himachal express (Train number 14553). It takes approximately 3.5 hrs to reach Mandi from Kiratpur by road. The Kiratpur Sahib railway station is very small and a bit far from the bus stand. All the buses coming from Delhi and Chandigarh towards Mandi, pass through Kiratpur Sahib.</ul>
<ul><strong>From Chandigarh:</strong></ul>
<ul style=text-align:justify><strong> By bus/taxi:</strong> From Chandigarh the buses to Mandi are available at Sector 43 bus stand. The other choice from Chandigarh to Mandi is to travel by taxi. The distance between Chandigarh and Mandi is ~200 kms. This distance can be covered in approximately 6 hrs by bus. By taxi, it usually takes around 5 hrs.</ul>
