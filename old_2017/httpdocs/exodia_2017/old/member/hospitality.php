                <h3>FAQs</h3>
                <b>1. We are a group of friends not participating in any of the major competitions and just coming to Exodia to have fun. Would we be allowed?</b><br/>
                Yes, you can come to Exodia.<br/><br/>
                <b>2. I have received the hospitality form, what should I do next to avail accommodation?</b><br/>
                No further step is required. Just present the hard copy of the form when you report at the hospitality desk. The registration fee (Rs 900) which includes refundable security deposit (Rs. 100) is to be paid before you arrive at the campus for the festival (8th – 10th April), mode of payment being online.<br/><br/>
                <b>3. What is the procedure to be followed after we reach IIT Mandi?</b>
                <ul>
                <li>You need to report to the Hospitality Desk along with your college identity card.</li>
                <li>You will have to produce the reciept of your online payment and a recent passport size photograph.</li>
                <li>Booklet will act as your identity card in campus.</li>
                <li>In case of a big contingent, contingent leader needs to present the ID cards of all the people in his contingent at the hospitality desk along with a list of all the people. Accommodation would be strictly on shared basis.</li>
                </ul><br/>
                <b>4. My parents or professors want to accompany me, is this allowed?</b><br/>
                No, only the confirmed participants are allowed to enter the institute premises.<br/><br/>
                <b>5. Am I expected to pay the full registration amount even if I do not want accommodation facilities?</b><br/>
                Yes, the event registration amount has to be paid by every participant. The accommodation facilities are complimentary.<br/><br/>
                <b>6. Can two participants be from different colleges in a team event?</b><br/>
                Yes, Exodia  does not restrict cross participation from colleges in a team event.<br/><br/>
                <b>7. Can I get accommodation on 6th April or before, although the festival starts from 8th of April?</b><br/>
                No. Accommodation will be provided from 7th night. All the participants coming before can stay in hotels at Mandi.<br/><br/>
                <b>8. How do I avail traveling facility from Mandi Bus stand to IIT?</b><br/>
                <div style="text-align:justify">Mandi is a small town at the center of Himachal Pradesh, on the way to the popular resorts of Kullu and Manali. The main campus of IIT Mandi is situated in Kamand vilage, which is 14 kilometers away from Mandi town. IIT Mandi has several offices and a guest house in its transit campus in Mandi town. To reach IIT Mandi's main campus from Mandi town:<br/>
<b>By IIT bus:</b> IIT Mandi's shuttle buses ply regularly between Mandi and Kamand. The schedule for buses is given below. On regular working days, the first bus to Kamand is at 8:00 AM, and the last bus is at 8:30 PM. The bus route starts at IIT Mandi Kamand campus and end at Vallabh Degree College, Mandi – the transit campus of IIT Mandi.<br/>
<b>By taxi:</b> Taxis are stationed outside Mandi main bus stand 24 x 7. The cost of a one-way trip to the IIT campus is about Rs. 600/-.<br/>
